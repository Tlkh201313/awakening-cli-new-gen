export const BANNER_WIDTH = 66

export const BOX_WIDTH = BANNER_WIDTH

export const WORDMARK = "‚óÜ AWAKENED ‚óÜ"

export function formatBannerLine(line: string, width = BANNER_WIDTH) {
  return line.padEnd(width)
}

export function wordmarkLine(width = BANNER_WIDTH) {
  const text = WORDMARK
  if (text.length >= width) return text.slice(0, width)
  const pad = width - text.length
  const left = Math.floor(pad / 2)
  return `${" ".repeat(left)}${text}${" ".repeat(pad - left)}`
}

export type StatusBoxInput = {
  provider?: string
  model?: string
  endpoint?: string
  packs?: string
  ready?: boolean
  status?: string
  hint?: string
}

function clip(value: string, max: number) {
  if (value.length <= max) return value
  if (max <= 1) return value.slice(0, max)
  return value.slice(0, max - 1) + "‚Ä¶"
}

function row(label: string, value: string, width = BOX_WIDTH) {
  const inner = width - 4
  const content = `${label.padEnd(10)}${clip(value, inner - 10)}`
  return `‚Ēā ${content.padEnd(inner)} ‚Ēā`
}

function boxFrame(width: number) {
  const inner = width - 4
  return {
    top: `‚óÜ${"‚ĒÄ".repeat(width - 2)}‚óÜ`,
    mid: `‚Ēú${"‚ĒÄ".repeat(width - 2)}‚Ē§`,
    bottom: `‚óÜ${"‚ĒÄ".repeat(width - 2)}‚óÜ`,
    inner,
  }
}

export function statusBox(input: StatusBoxInput, width = BOX_WIDTH) {
  const frame = boxFrame(width)
  const status = input.status ?? (input.ready ? "Ready ‚ÄĒ start building" : "Connect a provider ‚ÄĒ /connect")
  const hint = input.hint ?? "(/help for commands)"
  const mode = input.ready ? "cloud" : "offline"
  const statusLine = `‚óÜ ${mode}    ${status}    ${hint}`
  return [
    frame.top,
    row("Provider", input.provider ?? "‚ÄĒ", width),
    row("Model", input.model ?? "‚ÄĒ", width),
    row("Endpoint", input.endpoint ?? "‚ÄĒ", width),
    row("Packs", input.packs ?? "‚ÄĒ", width),
    frame.mid,
    `‚Ēā ${clip(statusLine, frame.inner).padEnd(frame.inner)} ‚Ēā`,
    frame.bottom,
  ]
}

export function statusBoxSlim(input: StatusBoxInput, width = BOX_WIDTH) {
  const frame = boxFrame(width)
  const status = input.status ?? (input.ready ? "Ready ‚ÄĒ start building" : "Connect a provider ‚ÄĒ /connect")
  const hint = input.hint ?? "(/help for commands)"
  const mode = input.ready ? "cloud" : "offline"
  const summary = clip(`${input.provider ?? "‚ÄĒ"} ¬∑ ${input.model ?? "‚ÄĒ"} ¬∑ ${input.packs ?? "‚ÄĒ"}`, frame.inner - 2)
  const statusLine = `‚óÜ ${mode}  ${summary}`
  return [
    frame.top,
    `‚Ēā ${clip(statusLine, frame.inner).padEnd(frame.inner)} ‚Ēā`,
    `‚Ēā ${clip(`${status}  ${hint}`, frame.inner).padEnd(frame.inner)} ‚Ēā`,
    frame.bottom,
  ]
}

